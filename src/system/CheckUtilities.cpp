    unsigned short
    Crc16Utilities::checkByBit(const uint8_t *buffer, off_t offset, size_t count, unsigned short initValue,
                               unsigned short polynomial) {
        for (off_t i = offset; i < (off_t) count + offset; i++) {
            initValue = (unsigned short) (initValue ^ buffer[i]);
            for (int j = 0; j < 8; j++) {
                int msbInfo = initValue & 0x0001;
                initValue = (unsigned short) (initValue >> 1);
                if (msbInfo != 0) {
                    initValue = (unsigned short) (initValue ^ polynomial);
                }
            }
        }
        return initValue;
    }

    unsigned short
    Crc16Utilities::checkByBit(const ByteArray &buffer, unsigned short initValue, unsigned short polynomial) {
        return checkByBit(buffer.data(), 0, buffer.count(), initValue, polynomial);
    }

    unsigned short
    Crc16Utilities::quickCheckByBit(const uint8_t *buffer, off_t offset, size_t count, unsigned short initValue) {
        uint8_t u8CRCHi; // high uint8_t of CRC initialized
        uint8_t u8CRCLo; // low uint8_t of CRC initialized
        uint8_t u8Index; // will index into CRC lookup table

        u8CRCLo = initValue & 0xFF;
        u8CRCHi = (initValue >> 8) & 0xFF;
        for (off_t i = offset; i < (off_t) count + offset; i++) {
            // calculate the CRC
            u8Index = u8CRCHi ^ buffer[i];
            u8CRCHi = u8CRCLo ^ s_aCRCHi[u8Index];
            u8CRCLo = s_aCRCLo[u8Index];
        }
        return ((u8CRCLo << 8) | u8CRCHi);
    }

    unsigned short Crc16Utilities::quickCheckByBit(const ByteArray &buffer, unsigned short initValue) {
        return quickCheckByBit(buffer.data(), 0, buffer.count(), initValue);
    }

